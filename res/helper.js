export function dayOfWeekShort(num) {
  switch (num) {
    case 1:
      return "MON"
    case 2:
      return "TUE"
    case 3:
      return "WED"
    case 4:
      return "THU"
    case 5:
      return "FRI"
    case 6:
      return "SAT"
    case 0:
      return "SUN"
  }
}

export function dayOfWeek(num) {
  switch (num) {
    case 1:
      return "Monday"
    case 2:
      return "Tuesday"
    case 3:
      return "Wednesday"
    case 4:
      return "Thursday"
    case 5:
      return "Friday"
    case 6:
      return "Saturday"
    case 0:
      return "Sunday"
  }
}